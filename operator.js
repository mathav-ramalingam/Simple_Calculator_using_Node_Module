exports.addition = function(a,b) 
{
    return a+b;
}

exports.subtraction = function(a,b) 
{
    return a-b;
}

exports.multiplication = function(a,b) 
{
    return a*b;
}

exports.division = function(a,b) 
{
    return a/b;
}

exports.modulus = function(a,b)
{
    return a%b;
}

exports.power = function(a,b)
{
    return a**b;
}